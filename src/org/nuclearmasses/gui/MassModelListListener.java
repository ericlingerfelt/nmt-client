package org.nuclearmasses.gui;

/**
 * This interface provides a method in response when a {@link javax.swing.JList} element is double-clicked.
 *
 * @see MassModelListEvent
 */
public interface MassModelListListener {
	
	/**
	 * Fired when a {@link javax.swing.JList} element is double-clicked.
	 */
	public void listDoubleClicked();
}
